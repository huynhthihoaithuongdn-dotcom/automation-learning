// Viết arrow functions

// 1. square
const square = (x) => x*x;
console.log('square(5):', square(5));

// 2. multiply
const multiply = (a,b) => a*b;
console.log('multiply(3, 7):', multiply(3,7));

// 3. greetUser
const greetUser = (name) => 'Xin chao, ' + name + '!';
console.log('greetUser:', greetUser('Khanh'));
