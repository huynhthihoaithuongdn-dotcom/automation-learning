let fruits = ['apple', 'banana', 'orange'];
// Add element

// add to the end of array
fruits.push('durian');      
console.log(fruits);

// remove the last element of array
fruits.pop();
console.log(fruits);

// add to the beginning of array
fruits.unshift('dragon fruit');
console.log(fruits);

// remove the first element of array
fruits.shift();
console.log(fruits);