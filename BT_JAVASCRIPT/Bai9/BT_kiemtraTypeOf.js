// Kiểm tra kiểu DL của các GT sau và in ra console: 
"hello", 42, true, null, undefined, [1, 2, 3]

//In mỗi dòng theo format: "typeof [giá trị] = [kiểu]"
// Riêng array, thêm dòng kiểm tra Array.isArray()


console.log('typeof hello =', typeof 'hello')
console.log('typeof 42 =', typeof 42)
console.log('typeof true =', typeof true)
console.log('typeof null =', typeof null)
console.log('typeof undefined =', typeof undefined)
console.log('typeof [1,2,3] =', typeof [1,2,3])
console.log('Array.isArray([1,2,3]) =', Array.isArray([1,2,3]))   // output: true 